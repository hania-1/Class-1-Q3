# 1: Integer (int)
# Pura number, bina decimal ke (Positive, Negative, Zero)

age = 20
temperature = -5
zero = 0
print(age, temperature, zero)  # Output: 20 -5 0

# 2: Float (float)
# Decimal waalay numbers

price = 19.99
height = 5.7
negative_float = -2.5
print(price, height, negative_float)  # Output: 19.99 5.7 -2.5

# 3: String (str)
# Textual data, har wo cheez jo quotes (" " ya ' ') mein ho

name = "Hania"
greeting = 'Good Morning'
print(name, greeting)  # Output: Hania Good Morning

# 4: List (list)
# Cheezon ki list (Order wali aur badal sakti hai)

fruits = ["Apple", "Banana", "Mango"]
print(fruits)  # Output: ['Apple', 'Banana', 'Mango']

# 5: Tuple (tuple)
# List jaisi hi hoti hai, lekin iski cheezain change nahi hoti (Immutable)

colors = ("Red", "Green", "Blue")
print(colors)  # Output: ('Red', 'Green', 'Blue')

# 6: Dictionary (dict)
# Key-Value pairs, jahan hum cheezon ko naam se access karte hain

student = {
    "name": "Hania",
    "age": 20
}
print(student)  # Output: {'name': 'Hania', 'age': 20}

# 7: Set (set)
# Unique values ki collection, repeated values ko remove kar deta hai

unique_numbers = {1, 2, 3, 3, 4}
print(unique_numbers)  # Output: {1, 2, 3, 4}

# 8: Boolean (bool)
# Sirf do values: True ya False

is_happy = True
is_raining = False
print(is_happy, is_raining)  # Output: True False

# 9: None Type (None)
# Jab value na ho, empty ya unknown value dikhane ke liye

result = None
print(result)  # Output: None
